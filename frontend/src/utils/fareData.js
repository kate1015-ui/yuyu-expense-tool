export function lookupFare(tool, origin, destination) {
  if (!origin || !destination) return null;

  if (tool === "高鐵") {
    const i = THSR_STATIONS.indexOf(origin);
    const j = THSR_STATIONS.indexOf(destination);
    if (i >= 0 && j >= 0) return THSR_FARE_MATRIX[i][j];
  }

  if (tool === "台鐵") {
    const key = `${origin}-${destination}`;
    const rev = `${destination}-${origin}`;
    return TRA_FARES[key] ?? TRA_FARES[rev] ?? null;
  }

  return null;
}
